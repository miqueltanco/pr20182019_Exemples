package pru05;

import java.util.Date;

public class Empleado {
	
		//VARIABLES
	
        int emp_no;
       	Date birth_date;
        String first_name;
        String last_name;
        
        
		public Empleado(int emp_no, Date birth_date, String first_name, String last_name) {
			this.emp_no = emp_no;
			this.birth_date = birth_date;
			this.first_name = first_name;
			this.last_name = last_name;
		}
        
        
		
		
	
}
